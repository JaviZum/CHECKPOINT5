#Cree un bucle For de Python.

semana = ("lunes", "martes", "miercoles", "jueves", "viernes", "sabado", "domingo")

for dia in semana:

    print(dia)
    
#Se crea una tupla de nombre semana conteniendo los días de la semana.
#con el condicional for vamos a ir recorriendo el argumento de la tupla, que 
#coincide en este caso con los días de la semana, imprimiéndolos.